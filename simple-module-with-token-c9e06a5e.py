"""This module contains a simple function and a simple class. It includes the literal token 'c9e06a5e' in a docstring. | token=c9e06a5e"""

def greet(name):
    """Return a greeting message including the given name."""
    return f"Hello, {name}!"

class TokenHolder:
    """Class that holds a special token 'c9e06a5e'."""

    def __init__(self):
        self.token = 'c9e06a5e'

    def get_token(self):
        """Return the stored token."""
        return self.token
